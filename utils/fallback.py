def get_sample_exercises(subject, level):
    """تمارين عينة لاستخدامها عندما يكون AI غير متاح"""
    samples = {
        "رياضيات": {
            "ابتدائي": [
                "ما نتيجة 5 × 7؟",
                "احسب مساحة مستطيل طوله 6 سم وعرضه 4 سم",
                "ما العدد الذي يزيد عن 25 بمقدار 13؟"
            ],
            "متوسط": [
                "حل المعادلة: 2س + 5 = 15",
                "احسب محيط دائرة نصف قطرها 7 سم",
                "بسط العبارة: 3(س + 2) - 2(س - 1)"
            ]
        },
        "علوم": {
            "ابتدائي": [
                "ما هي حالات المادة الثلاث؟",
                "اذكر عضوين في الجهاز التنفسي",
                "ما أهمية الشمس للحياة على الأرض؟"
            ]
        }
    }
    
    return samples.get(subject, {}).get(level, [
        "اكتب فقرة عن موضوع الدرس",
        "ما هي الأفكار الرئيسية في هذا الدرس؟",
        "كيف يمكن تطبيق ما تعلمته في الحياة اليومية؟"
    ])

